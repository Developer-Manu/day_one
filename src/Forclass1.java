public class Forclass1 {

    public void class1(){
        for(int i=0; i<10; i++ )
        {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
    }

    public void class2(){
        for(int i=0; i<10; i++)
        {
            for(int j=0; j<10; j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }

    public void class3(){
        for(int i=0; i<=10; i++)
        {
            for(int j=0; j<i; j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }

    public void class4(){
        for(int i=10; i>=0; i--)
        {
            for(int j=i; j>=0; j--)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }

    public void class5(){

    }
}
